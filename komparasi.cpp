#include <iostream>
using namespace std;
int main()
{
    int a = 99;
    int b = 99;
    /*
    Operator komparasi
    - == : sama dengan
    - < : lebih kecil
    - <= : lebih kecil sama dengan
    - > : lebih besar
    - >= : lebih besar sama dengan
    - != : tidak sama dengan
    */
    bool compare = a == b;
    // output : 1 (true)
    cout << compare << endl;
}